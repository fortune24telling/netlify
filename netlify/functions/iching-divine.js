// ===== Vercel サーバーレス関数のエントリーポイント =====
// POST /api/divine
// body: { mode: "free" | "theme" | "question", category?: string }
//   mode "free"     … 無料診断（問い固定、結果は卦名+一言のみ）
//   mode "theme"    … ¥500診断・テーマ選択（category必須）
//   mode "question" … ¥500診断・自由記述問い（結果は卦全体の一言解釈）
module.exports = (req, res) => {
  if (req.method !== "POST") {
    res.status(405).json({ error: "Method Not Allowed" });
    return;
  }

  let body = req.body;
  if (typeof body === "string") {
    try { body = JSON.parse(body); } catch (e) { body = {}; }
  }
  const mode = (body && body.mode) || "free";
  const category = body && body.category;

  const hexagram = buildHexagram();
  const coreData = hexCore[hexagram.key];

  let payload = {
    name: hexagram.name,
    throws: hexagram.throws,
    changingCount: hexagram.changingCount
  };

  if (mode === "free") {
    // 無料診断：卦名＋一言のみ。変爻の詳細やテーマ解釈は含めない
    payload.interpretation = {
      type: "brief",
      text: hexBrief[hexagram.key] || ""
    };
  } else if (mode === "theme" && category && themeTemplates[category]) {
    const tpl = themeTemplates[category];
    const sentence = (tpl[coreData.tone] || tpl.active).replace("{core}", coreData.core);
    payload.interpretation = {
      type: "theme",
      label: tpl.label,
      text: sentence
    };
    payload.planGuide = buildPlanGuide(hexagram.changingCount);
  } else {
    // 自由記述の問い：卦全体の一言解釈
    payload.interpretation = {
      type: "brief",
      text: hexBrief[hexagram.key] || ""
    };
    payload.planGuide = buildPlanGuide(hexagram.changingCount);
  }

  res.status(200).json(payload);
};
