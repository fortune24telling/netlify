// ===== コインを1回投げて、表/裏の結果を返す（サーバー側で乱数生成） =====
function tossThreeCoins() {
  const results = [0, 1, 2].map(() => (Math.random() < 0.5 ? "omote" : "ura"));
  const sum = results.reduce((acc, r) => acc + (r === "omote" ? 3 : 2), 0);
  let yin, changing;
  if (sum === 6) { yin = true;  changing = true;  }
  if (sum === 7) { yin = false; changing = false; }
  if (sum === 8) { yin = true;  changing = false; }
  if (sum === 9) { yin = false; changing = true;  }
  return { results, sum, yin, changing };
}

// ===== 6回分のコイン投げを行い、卦を立てる =====
function buildHexagram() {
  const throws = [];
  const lines = [];
  let changingCount = 0;

  for (let i = 0; i < 6; i++) {
    const t = tossThreeCoins();
    lines.push(t.yin ? "0" : "1");
    if (t.changing) changingCount++;
    throws.push({
      // ブラウザにはコインの表裏アニメーション用の情報だけ返す
      coins: t.results,
      yin: t.yin,
      changing: t.changing
    });
  }

  const key = lines.join("");
  const name = hexNames[key] || "卦";

  return { key, name, throws, changingCount };
}

