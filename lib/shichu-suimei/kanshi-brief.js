// kanshi-brief.js
// 60干支（日柱）それぞれの一言診断テキスト。
// 十干（核となる性質）×十二支（表れ方・性質の色合い）の組み合わせで作成。
// 無料診断では、この一言診断のみを表示する（¥500診断ではここに加えて
// 月柱・年柱・時柱を含めた全体像と、今年の運気の解釈を加える）。

const KAN_CORE = {
  "甲": "まっすぐに伸びていく、芯の強さ",
  "乙": "しなやかに形を変えながら進む、柔軟さ",
  "丙": "周囲を明るく照らす、おおらかさ",
  "丁": "静かに、しかし確かに灯る、繊細さ",
  "戊": "どっしりと構え、揺らがない、安定感",
  "己": "受け止め、育てる、包容力",
  "庚": "迷いなく断ち切る、潔さ",
  "辛": "磨かれて輝きを増す、洗練さ",
  "壬": "とどまらず流れ続ける、自由さ",
  "癸": "静かに染み渡る、深い洞察力"
};

const SHI_FLAVOR = {
  "子": "機を見て動く、機転の良さが加わります",
  "丑": "地道に積み重ねる、忍耐強さが加わります",
  "寅": "前に踏み出す、勢いの良さが加わります",
  "卯": "周囲と調和する、柔らかさが加わります",
  "辰": "大きな器で物事を捉える、度量の広さが加わります",
  "巳": "深く集中する、探究心の強さが加わります",
  "午": "情熱的に動く、表現力の豊かさが加わります",
  "未": "穏やかに気を配る、思いやりの深さが加わります",
  "申": "機敏に立ち回る、要領の良さが加わります",
  "酉": "美しく形を整える、こだわりの強さが加わります",
  "戌": "義理を大切にする、誠実さが加わります",
  "亥": "純粋にまっすぐ向き合う、素直さが加わります"
};

function getKanshiBrief(kan, shi) {
  const core = KAN_CORE[kan] || "";
  const flavor = SHI_FLAVOR[shi] || "";
  return `${kan}${shi}の日に生まれたあなたは、${core}を持つ人です。そこに、${flavor}。`;
}

module.exports = { KAN_CORE, SHI_FLAVOR, getKanshiBrief };
